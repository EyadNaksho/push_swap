#include "ft_printf.h"

int	ft_found_s(char *s)
{
	int	i;

	i = 0;
	if (!s)
		s = "(null)";
	while (*s)
	{
		i += ft_putchar(*s);
		if (i < 0)
			return (-1);
		s++;
	}
	return (i);
}
